const NOMBRES_FORMACION = Object.keys(FORMACIONES_MAP);

function hashEquipo(equipoId) {
  return equipoId.split("").reduce((a, c) => a + c.charCodeAt(0), 0);
}

export function obtenerFormacion(equipoId) {
  const idx = hashEquipo(equipoId) % NOMBRES_FORMACION.length;
  return FORMACIONES_MAP[NOMBRES_FORMACION[idx]];
}


function mezclar(arr, semilla) {
  const r = [...arr];
  let s = semilla;
  for (let i = r.length - 1; i > 0; i--) {
    s = (s * 16807 + 1) % 2147483647;
    const j = s % (i + 1);
    [r[i], r[j]] = [r[j], r[i]];
  }
  return r;
}

export function generarPlantel(equipoId, formacion) {
  const cacheKey = equipoId + (formacion ? formacion.label : "");
  if (cachePlanteles.has(cacheKey)) return cachePlanteles.get(cacheKey);

  if (PLANTELES_FIJOS[equipoId]) {
    const plantel = PLANTELES_FIJOS[equipoId].map((j, i) => ({
      nombreCompleto: j.nombre,
      posicion: j.pos,
      dorsal: (i + 1).toString(),
      titular: i < (formacion ? formacion.posiciones.length : 11),
      foto: j.foto || null,
    }));
    cachePlanteles.set(cacheKey, plantel);
    return plantel;
  }

  const region = REGION_POOL[equipoId] || "es";
  const pool = NOMBRES[region] || NOMBRES.es;
  const semilla = equipoId.split("").reduce((a, c) => a + c.charCodeAt(0), 0);

  const nombresMezclados = mezclar(pool.nombres, semilla);
  const apellidosMezclados = mezclar(pool.apellidos, semilla + 999);

  const plantel = [];
  const TOTAL = 26;

  const posicionesBase = formacion ? formacion.posiciones : [
    { key: "POR", row: 5, x: 50 },
    { key: "DFI", row: 4, x: 12 },
    { key: "DFC", row: 4, x: 38 },
    { key: "DFC", row: 4, x: 62 },
    { key: "DFD", row: 4, x: 88 },
    { key: "MCI", row: 3, x: 20 },
    { key: "MCC", row: 3, x: 50 },
    { key: "MCD", row: 3, x: 80 },
    { key: "EXI", row: 2, x: 15 },
    { key: "DC",  row: 2, x: 50 },
    { key: "EXD", row: 2, x: 85 },
  ];

  const suplentes = [
    { key: "SUP", row: 0, x: 50 },
    { key: "SUP", row: 0, x: 50 },
    { key: "SUP", row: 0, x: 50 },
    { key: "SUP", row: 0, x: 50 },
    { key: "SUP", row: 0, x: 50 },
    { key: "SUP", row: 0, x: 50 },
    { key: "SUP", row: 0, x: 50 },
    { key: "SUP", row: 0, x: 50 },
    { key: "SUP", row: 0, x: 50 },
    { key: "SUP", row: 0, x: 50 },
    { key: "SUP", row: 0, x: 50 },
    { key: "SUP", row: 0, x: 50 },
    { key: "SUP", row: 0, x: 50 },
    { key: "SUP", row: 0, x: 50 },
    { key: "SUP", row: 0, x: 50 },
  ];

  const todasPosiciones = [...posicionesBase, ...suplentes];

  for (let i = 0; i < TOTAL; i++) {
    const nombre = nombresMezclados[i % nombresMezclados.length];
    const apellido = apellidosMezclados[i % apellidosMezclados.length];
    const pos = todasPosiciones[i] || todasPosiciones[todasPosiciones.length - 1];
    const esTitular = i < posicionesBase.length;
    plantel.push({
      nombreCompleto: `${nombre} ${apellido}`,
      posicion: pos.key,
      dorsal: (i + 1).toString(),
      titular: esTitular,
    });
  }

  cachePlanteles.set(cacheKey, plantel);
  return plantel;
}
